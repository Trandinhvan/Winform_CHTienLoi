		SELECT SOLUONG
		FROM inserted
		WHERE MAHG = HANG.MAHG
	)
	FROM HANG
	JOIN inserted ON HANG.MAHG = inserted.MAHG
END


CREATE TRIGGER trg_TamTinh ON CHITIETHD AFTER INSERT AS 
BEGIN
	UPDATE HOADON
	SET TAMTINH = TAMTINH+(
		SELECT DONGIA*SOLUONG
		FROM inserted
		WHERE MAHD = HOADON.MAHD
	)
	FROM HOADON
	JOIN inserted ON HOADON.MAHD = inserted.MAHD
END

CREATE TRIGGER trg_ThanhToan ON HOADON AFTER UPDATE AS 
BEGIN
	UPDATE HOADON
	SET THANHTOAN = TAMTINH-GIAMGIA
END

CREATE TRIGGER trg_TinhTrang on HANG AFTER UPDATE AS
IF(SELECT SOLUONG
   FROM inserted)>0
BEGIN
	UPDATE HANG
	SET TINHTRANG=N'CÒN HÀNG'
	from Hang
	JOIN inserted ON HANG.MAHG = inserted.MAHG
END
ELSE
BEGIN
	 UPDATE HANG
	SET TINHTRANG=N'HẾT HÀNG'
	from Hang
	JOIN inserted ON HANG.MAHG = inserted.MAHG
END
	
