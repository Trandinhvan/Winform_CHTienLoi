CREATE TRIGGER trg_SoLuong ON CHITIETHD AFTER INSERT AS 
BEGIN
	UPDATE HANG
	SET SOLUONG = HANG.SOLUONG-(
		SELECT SOLUONG
		FROM inserted
		WHERE MAHG = HANG.MAHG
	)
	FROM HANG
	JOIN inserted ON HANG.MAHG = inserted.MAHG
END

