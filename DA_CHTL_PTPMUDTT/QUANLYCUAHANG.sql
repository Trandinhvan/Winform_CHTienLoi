CREATE TRIGGER trg_NhapHang ON CHITIETPN AFTER INSERT AS 
BEGIN
	UPDATE HANG
	SET DONGIA = 0+(
		SELECT DONGIA
		FROM inserted
		WHERE MAHG = HANG.MAHG
	)
	FROM HANG
	JOIN inserted ON HANG.MAHG = inserted.MAHG
END

CREATE TRIGGER trg_TienNhap ON CHITIETPN AFTER INSERT AS 
BEGIN
	UPDATE PHIEUNHAP
	SET TIENNHAP = PHIEUNHAP.TIENNHAP+(
		SELECT DONGIA*SOLUONG
		FROM inserted
		WHERE MAPN = PHIEUNHAP.MAPN
	)
	FROM PHIEUNHAP
	JOIN inserted ON PHIEUNHAP.MAPN = inserted.MAPN
END

